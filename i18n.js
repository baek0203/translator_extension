// 언어별로 지원되는 언어 코드 매핑
const LANG_CODE_MAP = {
  'zh-CN': 'zh-CN',
  'zh-TW': 'zh-CN', // 중국어 번체도 간체 UI 사용
  'ar': 'en',  // 아랍어는 영어 UI 사용
  'hi': 'en',  // 힌디어는 영어 UI 사용
  'th': 'en',  // 태국어는 영어 UI 사용
  'vi': 'en',  // 베트남어는 영어 UI 사용
  'id': 'en'   // 인도네시아어는 영어 UI 사용
};

// UI 언어 가져오기
function getUILanguage(langCode) {
  // 매핑된 언어 코드 사용
  const mappedCode = LANG_CODE_MAP[langCode] || langCode;

  // 해당 언어가 있으면 사용, 없으면 영어 기본
  return UI_TRANSLATIONS[mappedCode] || UI_TRANSLATIONS['en'];
}

// 메시지 가져오기
function getMessage(key, langCode) {
  const translations = getUILanguage(langCode);
  return translations[key] || key;
}
